//Abstraction is a process to hiding unnecessary details from the user.
//In Abstraction we will show only necessary information to the user.


import 'dart:math';

void main() {
  print(pow(2, 5));
  print(sqrt(64));
  print(sin(78));
  print(cos(65));
  print(tan(54));
  print(log(30));
  print(78.67.ceil());  // Use .ceil() on a double
  print(80.76.floor());  // Use .floor() on a double
}



